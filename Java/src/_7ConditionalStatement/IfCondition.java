package _7ConditionalStatement;

public class IfCondition {
	public static void main(String []args)
	{
		int x =7;
		int y =9;
		
		if (x>y)  // if condition is false then it will go to else 
			System.out.println("No_need_to_go_in_else_condition");  // If we are printing only one statement then no need to add brackets.
		else
			System.out.println("If condition false");
	}

}
