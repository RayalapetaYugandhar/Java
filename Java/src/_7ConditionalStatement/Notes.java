/*
 * 		When we execute a code, the flow goes to one direction and Suddenly there is an point in that point
 * our code is true it will go one direction and if code is false it will go another direction.
 * 
 * 										|
 * 										|
 * 										|CODE
 * 								true	|       false
 * 							-------------------------
 * 							|if						|else
 * 							|						|
 * 
 * EX:-
 * 		If the code is true, it will run if condition and if the code is false then it will run else condition. 
 * 
 * 
 *Note:-
 *		-> If we have 2 statements then we go for 'if else' condition.
 *
 *			EX:-
	 *				int x =8;
	 *				int y =7;
	 *
	 *				if(x>y)  //Here we are printing only one statement then no need to add brackets.
	 *					System.out.println(x);
	 *				else
	 *					System.out.println(y);
	 *
 *		-> If we have 3 statements then we go for 'else if' condition.
 *
 * 			EX:-
 * 					int x =8;
	 *				int y =7;
	 *				int z =6;
	 *
	 *				if(x>y && x>z)
	 *					System.out.println(x);
	 *				else if (y>x && y>z)
	 *					System.out.println(y);
	 *				else
	 *					System.out.println(z);
	 *
 * 
 * 
 */