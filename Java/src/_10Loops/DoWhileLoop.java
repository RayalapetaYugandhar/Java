package _10Loops;

public class DoWhileLoop {
	public static void main(String []args)
	{
		int i =1;
		do
		{
			System.out.println("Hi" + i);
			i++;
		}
		while(i<=4);
	}

}


// If condition is 'true' it will work normally as while loop. 

//If condition is 'false' then it will work as do while loop. 