/* 
 * OPERATORS:-
 * ---------------------------------------------------------
 * 1. Arithmetic operations:-
 * ------------------------------------------------------------
 * 	Operators      Description         Example
 * ------------------------------------------------------------
 * 		+          Addition (+)        10+20 =30
 * 		-          Subtraction (-)     20-10 =10
 * 		*          Multiplication (*)  10*20 = 200
 * 		/          Division (/)        20/10 = 2
 * 		%          Modulus (%)         20%10 = 0
 * ---------------------------------------------------------------
 * Note:-
 * 	* 1. Addition:- (+)
 * 		 In Addition we can add 2 or more values
 * 
	 * 2. Subtraction:- (-)
	 * 		In Subtraction we can Subtract 2 or more values
	 * 
	 * 3. Multiplication:- (*)
	 * 		In Multiplication we can Multiply 2 or more values
	 * 	
	 * 4. Division:- (/)
	 * 		In Division we can do division operation by using 2 values . 
	 * 	In division we will get only quotient, For Getting remainder we have to perform Modulus operation.  
	 * 
	 * 5. Modulus:- (%)
	 * 		In Modulus we can do Modulus operation by using 2 values . 
	 * 	For Getting remainder we have to perform Modulus operation.  
 * 

 * ---------------------------------------------------------------------
 * 2. Relational (Conditional) operators:-
 *  ------------------------------------------------------------------
 *  	= =    Is equal to                10==20 = false
 *  	!=     Not equal to               10!=20 = true
 *  	>      Greater than               20>10 = true
 *  	>=     Greater than or equal to   20>=10 = true
 *  	<      Less than                  20<10 = false
 *  	<=     Less than                  20<=20 = true
 *  -------------------------------------------------------------------
 * 3. Logical operators :-
 * 	--------------------------------------------------------------------
 * 		&&     Logical AND          	(20==10 && 20==10) = false
 *  	||     Logical OR               (5==10 || 20==20) = true
 *  	!      Logical not               !(10==20) = true
 *  
 *                  &&                 ||                   !
 *                  
 *                 T  T  = T       T   T  = T             T = F
 *                 T  F  = F       T   F  = T             F = T
 *                 F  T  = F       F   T  = T        
 *                 F  F  = F       F   F  = F 
 * -----------------------------------------------------------------------
 * Note:-
 * 		&& - If first statement is true then only it will check next statement.
 * 		|| - If First statement is true or false it will next statement.
 * 		If we have true and we want false then we can use ! operation .
 * 		If we have false and we want true then also we can use ! operation .
 * 
 * -------------------------------------------------------------------------
 * 4. Bitwise Operations:-
 * ------------------------------------------------------------------------------
 * 		&            Bitwise AND                   (10==20 & 20==30) = false 
 *  	|            Bitwise OR	                   (10==20 | 20==30) = false 
 *   	^            Bitwise XOR                   (10==20 ^ 20==30) = false 
 *    	~            Bitwise NOT                   (~10) = -10
 *     	<<           Bitwise LeftShift             (10<<2) = 40 
 *     	>>           Bitwise RightShift            (10>>2) = 2
 *      >>>          Bitwise RightShift with ZERO  (10>>>2) = 2 
 * ------------------------------------------------------------------------------- 
 * 
 * 5. Assignment operators:-
 * ------------------------------------------------------------------------------
 * Operator             Description                                  Example
 * --------------------------------------------------------------------------------
 * 	  =                   Assign                              10+20 = 30
 * 	  +=                 ADD and Assign                    var a =10; a+=20;  new a = 30;
 *    -=                 Subtract and Assign               var a =20; a-=10;  new a = 10;
 *    *=                 Multiply and Assign               var a =10; a*=20;  new a = 200;
 *    /=                 Divide and Assign                 var a =10; a/=2;   new a = 5;
 *    %=                 Modulus and Assign                var a =10; a%=2;  new a = 0;
 * ------------------------------------------------------------------------------------
  * 
 */